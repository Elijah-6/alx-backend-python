#!/usr/bin/env python3

"""
This module contains a function that converts a float to a string.
Functions:
    to_str(n: float) -> str: Converts a float to its string representation.
"""


def to_str(n: float) -> str:
    """
    Converts a floating point number to its string representation.

    Args:
        n (float): The floating point number to convert.

    Returns:
        str: The string representation of the floating point number.
    """
    return str(n)
